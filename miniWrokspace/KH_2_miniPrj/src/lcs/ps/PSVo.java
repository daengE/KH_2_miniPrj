package lcs.ps;

public class PSVo {
	
	private int no;
	private String q; 
	private String a;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getQ() {
		return q;
	}
	public void setQ(String q) {
		this.q = q;
	}
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	@Override
	public String toString() {
		return "PSVo [no=" + no + ", q=" + q + ", a=" + a + "]";
	} 
	
	
	
}
